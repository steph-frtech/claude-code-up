#!/usr/bin/env node
// Generates two TS modules consumed by the bundle:
//   1. src/templates.gen.ts  — text content from src/scripts/*.sh
//   2. src/catalog.gen.ts    — typed constants from catalog/*.json
//
// Run before tsc/tsx/tsup via "prebuild" / "predev" / "pretypecheck" npm scripts.

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

// ── Templates from shell scripts ──────────────────────────────────────
const SHELL_SOURCES = [
  { src: "src/scripts/statusline.sh", exportName: "STATUSLINE_SH" },
];

{
  let out = "// AUTO-GENERATED by scripts/gen-templates.mjs — do not edit.\n\n";
  for (const { src, exportName } of SHELL_SOURCES) {
    const content = await readFile(path.join(root, src), "utf8");
    out += `export const ${exportName}: string = ${JSON.stringify(content)};\n\n`;
  }
  const dest = path.join(root, "src/templates.gen.ts");
  await writeFile(dest, out, "utf8");
  console.log(`Generated ${path.relative(root, dest)}`);
}

// ── Catalog from JSON files ───────────────────────────────────────────
{
  const projectTypes = JSON.parse(
    await readFile(path.join(root, "catalog/project-types.json"), "utf8"),
  );
  const stack = JSON.parse(
    await readFile(path.join(root, "catalog/stack.json"), "utf8"),
  );
  const mcps = JSON.parse(
    await readFile(path.join(root, "catalog/mcps.json"), "utf8"),
  );
  const subagents = JSON.parse(
    await readFile(path.join(root, "catalog/subagents.json"), "utf8"),
  );
  const skills = JSON.parse(
    await readFile(path.join(root, "catalog/skills.json"), "utf8"),
  );
  const itemsWshobson = JSON.parse(
    await readFile(path.join(root, "catalog/items/wshobson.json"), "utf8"),
  );
  const itemsSuperpowers = JSON.parse(
    await readFile(path.join(root, "catalog/items/superpowers.json"), "utf8"),
  );
  const itemsPocock = JSON.parse(
    await readFile(path.join(root, "catalog/items/pocock.json"), "utf8"),
  );
  const scaffolders = JSON.parse(
    await readFile(path.join(root, "catalog/scaffolders.json"), "utf8"),
  );
  const commandBundles = JSON.parse(
    await readFile(path.join(root, "catalog/command-bundles.json"), "utf8"),
  );

  // Tiny consistency checks.
  const requireArray = (val, name) => {
    if (!Array.isArray(val)) {
      throw new Error(`${name} must be an array`);
    }
  };
  requireArray(projectTypes.categories, "project-types.json.categories");
  requireArray(projectTypes.databases, "project-types.json.databases");
  requireArray(stack.items, "stack.json.items");
  requireArray(mcps.servers, "mcps.json.servers");
  requireArray(subagents.agents, "subagents.json.agents");
  requireArray(skills.skills, "skills.json.skills");
  requireArray(itemsWshobson.items, "items/wshobson.json.items");
  requireArray(itemsSuperpowers.items, "items/superpowers.json.items");
  requireArray(itemsPocock.items, "items/pocock.json.items");
  requireArray(scaffolders.scaffolders, "scaffolders.json.scaffolders");
  requireArray(commandBundles.bundles, "command-bundles.json.bundles");
  for (const b of commandBundles.bundles) {
    if (!b.id || !b.commands) throw new Error(`command-bundle missing id/commands: ${JSON.stringify(b).slice(0, 80)}`);
    if (!Array.isArray(b.commands)) throw new Error(`command-bundle.commands must be array: ${b.id}`);
    for (const c of b.commands) {
      if (!c.exec) throw new Error(`command-bundle ${b.id} has command without exec: ${JSON.stringify(c).slice(0, 80)}`);
    }
  }

  // Validate items: every item has a tier; skip has reason; default has no applyWhen.
  const validateItems = (sourceName, items) => {
    for (const it of items) {
      if (!it.id || !it.tier) {
        throw new Error(`${sourceName}: missing id or tier on item ${JSON.stringify(it).slice(0, 100)}`);
      }
      if (!["default", "conditional", "skip"].includes(it.tier)) {
        throw new Error(`${sourceName}/${it.id}: invalid tier "${it.tier}"`);
      }
      if (it.tier === "skip" && !it.reason) {
        throw new Error(`${sourceName}/${it.id}: skip tier requires reason`);
      }
      if (it.tier === "default" && it.applyWhen !== undefined) {
        throw new Error(`${sourceName}/${it.id}: default tier must not have applyWhen`);
      }
    }
  };
  validateItems("wshobson", itemsWshobson.items);
  validateItems("superpowers", itemsSuperpowers.items);
  validateItems("pocock", itemsPocock.items);

  // Schema v2 — funnel structure with separate dimensions.
  if (projectTypes.schemaVersion !== "2") {
    throw new Error(
      `project-types.json schemaVersion must be "2", got ${projectTypes.schemaVersion}`,
    );
  }

  const out =
    "// AUTO-GENERATED by scripts/gen-templates.mjs — edit catalog/*.json instead.\n" +
    "\n" +
    "export interface FunnelOption { id: string; name: string }\n" +
    "\n" +
    `export const PROJECT_CATEGORIES: ReadonlyArray<FunnelOption> = ${JSON.stringify(projectTypes.categories, null, 2)};\n` +
    "\n" +
    `export const LANGUAGES_BY_CATEGORY: Record<string, ReadonlyArray<FunnelOption>> = ${JSON.stringify(projectTypes.languagesByCategory, null, 2)};\n` +
    "\n" +
    `export const FRAMEWORKS_BY_LANG_CATEGORY: Record<string, ReadonlyArray<FunnelOption>> = ${JSON.stringify(projectTypes.frameworksByLangCategory, null, 2)};\n` +
    "\n" +
    `export const DATABASES: ReadonlyArray<FunnelOption> = ${JSON.stringify(projectTypes.databases, null, 2)};\n` +
    "\n" +
    `export const ORMS_BY_LANGUAGE: Record<string, ReadonlyArray<FunnelOption>> = ${JSON.stringify(projectTypes.ormsByLanguage, null, 2)};\n` +
    "\n" +
    "export interface StackItemRaw {\n" +
    "  id: string;\n" +
    "  category: string;\n" +
    "  name: string;\n" +
    "  description: string;\n" +
    "  source: string;\n" +
    "  scope: \"project\" | \"user-documented\";\n" +
    "  hint: string;\n" +
    "  tags: string[];\n" +
    "  preselectFor: string[];\n" +
    "  installer: string;\n" +
    "  available: Record<string, number>;\n" +
    "  instructions?: string[];\n" +
    "  defaultPlugins?: string[];\n" +
    "  defaultSkills?: string[];\n" +
    "}\n" +
    `export const STACK_ITEMS: ReadonlyArray<StackItemRaw> = ${JSON.stringify(stack.items, null, 2)};\n` +
    "\n" +
    "export interface McpServerRaw {\n" +
    "  id: string;\n" +
    "  name: string;\n" +
    "  description: string;\n" +
    "  rating: number;\n" +
    "  tags: string[];\n" +
    "  preselectFor: string[];\n" +
    "  applyWhen?: {\n" +
    "    categories?: string[];\n" +
    "    languages?: string[];\n" +
    "    frameworks?: string[];\n" +
    "    databases?: string[];\n" +
    "    orms?: string[];\n" +
    "  };\n" +
    "  config: { command: string; args: string[]; env?: Record<string, string> };\n" +
    "  requiresEnvVars?: string[];\n" +
    "  envHint?: string;\n" +
    "  test?: { tool: string; args?: Record<string, unknown>; expectField?: string; expectPattern?: string };\n" +
    "}\n" +
    `export const MCP_SERVERS: ReadonlyArray<McpServerRaw> = ${JSON.stringify(mcps.servers, null, 2)};\n` +
    "\n" +
    "export interface CuratedAgentRaw {\n" +
    "  filename: string;\n" +
    "  name: string;\n" +
    "  content: string;\n" +
    "}\n" +
    `export const CURATED_AGENTS_DATA: ReadonlyArray<CuratedAgentRaw> = ${JSON.stringify(subagents.agents, null, 2)};\n` +
    "\n" +
    "export interface CuratedSkillRaw {\n" +
    "  id: string;\n" +
    "  name: string;\n" +
    "  description: string;\n" +
    "  content: string;\n" +
    "}\n" +
    `export const CURATED_SKILLS_DATA: ReadonlyArray<CuratedSkillRaw> = ${JSON.stringify(skills.skills, null, 2)};\n` +
    "\n" +
    "export interface CatalogItemRaw {\n" +
    "  id: string;\n" +
    "  kind: \"agent\" | \"skill\" | \"command\";\n" +
    "  container?: string;\n" +
    "  tier: \"default\" | \"conditional\" | \"skip\";\n" +
    "  description?: string;\n" +
    "  applyWhen?: {\n" +
    "    categories?: string[];\n" +
    "    languages?: string[];\n" +
    "    frameworks?: string[];\n" +
    "    databases?: string[];\n" +
    "    orms?: string[];\n" +
    "  };\n" +
    "  reason?: string;\n" +
    "  mappedFrom?: string;\n" +
    "}\n" +
    `export const WSHOBSON_ITEMS: ReadonlyArray<CatalogItemRaw> = ${JSON.stringify(itemsWshobson.items, null, 2)};\n` +
    `export const SUPERPOWERS_ITEMS: ReadonlyArray<CatalogItemRaw> = ${JSON.stringify(itemsSuperpowers.items, null, 2)};\n` +
    `export const POCOCK_ITEMS: ReadonlyArray<CatalogItemRaw> = ${JSON.stringify(itemsPocock.items, null, 2)};\n` +
    "\n" +
    "export interface ScaffolderTemplate {\n" +
    "  id: string;\n" +
    "  name: string;\n" +
    "  description?: string;\n" +
    "  /** Args appended to `args` when this template is picked. */\n" +
    "  extraArgs: string[];\n" +
    "  /** True for the entry shown first / pre-selected (the upstream default). */\n" +
    "  default?: boolean;\n" +
    "}\n" +
    "export interface ScaffolderRaw {\n" +
    "  id: string;\n" +
    "  name: string;\n" +
    "  description: string;\n" +
    "  command: string;\n" +
    "  args: string[];\n" +
    "  matchFrameworks: string[];\n" +
    "  /** If present, ccup prompts the user to pick a template before running. */\n" +
    "  templates?: ScaffolderTemplate[];\n" +
    "}\n" +
    `export const SCAFFOLDERS: ReadonlyArray<ScaffolderRaw> = ${JSON.stringify(scaffolders.scaffolders, null, 2)};\n` +
    "\n" +
    "export interface CommandBundleCommand { exec: string; desc?: string }\n" +
    "export interface CommandBundleRaw {\n" +
    "  id: string;\n" +
    "  name: string;\n" +
    "  description?: string;\n" +
    "  applyWhen?: {\n" +
    "    categories?: string[];\n" +
    "    languages?: string[];\n" +
    "    frameworks?: string[];\n" +
    "    databases?: string[];\n" +
    "    orms?: string[];\n" +
    "  };\n" +
    "  commands: CommandBundleCommand[];\n" +
    "}\n" +
    `export const COMMAND_BUNDLES: ReadonlyArray<CommandBundleRaw> = ${JSON.stringify(commandBundles.bundles, null, 2)};\n`;

  const dest = path.join(root, "src/catalog.gen.ts");
  await writeFile(dest, out, "utf8");
  console.log(`Generated ${path.relative(root, dest)}`);
}
