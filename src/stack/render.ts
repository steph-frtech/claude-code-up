import type { StackReport } from "../types.js";

export function renderStackMd(report: StackReport): string {
  const projectComps = report.components.filter((c) => c.scope === "project");
  const userComps = report.components.filter((c) => c.scope === "user-documented");

  const lines: string[] = [];
  lines.push("# Claude Stack");
  lines.push("");
  lines.push(`Generated by ${report.createdBy} on ${report.createdAt}.`);
  lines.push("");
  lines.push("Machine-readable inventory: `.claude/stack.json`");
  lines.push("");

  if (projectComps.length > 0) {
    lines.push("## Installed in this project (project-scope)");
    lines.push("");
    for (const c of projectComps) {
      lines.push(`### ${c.name}`);
      lines.push(`Source: <${c.source}>`);
      lines.push("");
      for (const item of c.items) {
        const mark = item.exists ? "✓" : "✗";
        const extras: string[] = [];
        if (item.count !== undefined) {
          extras.push(`${item.count} files`);
        } else if (item.bytes !== undefined) {
          extras.push(`${item.bytes} bytes`);
        }
        if (item.executable !== undefined) {
          extras.push(item.executable ? "executable" : "not executable");
        }
        const extraStr = extras.length ? ` _(${extras.join(", ")})_` : "";
        lines.push(`- [${mark}] \`${item.path}\` — ${item.name}${extraStr}`);
      }
      if (c.error) {
        lines.push(`- ⚠ Error: ${c.error}`);
      }
      lines.push("");
    }
  }

  if (userComps.length > 0) {
    lines.push("## To install yourself (user-scope, not run by ccup)");
    lines.push("");
    for (const c of userComps) {
      lines.push(`### ${c.name}`);
      lines.push(`Source: <${c.source}>`);
      if (c.available) {
        const parts = Object.entries(c.available).map(([k, v]) => `${v} ${k}`);
        lines.push(`Provides: ${parts.join(", ")}`);
      }
      lines.push("");
      if (c.instructions) {
        for (const line of c.instructions) {
          lines.push(`    ${line}`);
        }
        lines.push("");
      }
    }
  }

  lines.push("## Totals");
  lines.push("");
  const projTot = formatTotals(report.totals.project);
  const availTot = formatTotals(report.totals.available);
  lines.push(`- **Installed in project**: ${projTot || "nothing"}`);
  lines.push(`- **Available after user-scope installs**: ${availTot || "nothing"}`);
  lines.push("");

  return lines.join("\n");
}

function formatTotals(totals: Record<string, number>): string {
  const entries = Object.entries(totals).filter(([, v]) => v > 0);
  if (entries.length === 0) return "";
  return entries.map(([k, v]) => `${v} ${k}`).join(", ");
}

export function summarizeTotals(report: StackReport): string {
  const proj = formatTotals(report.totals.project);
  const avail = formatTotals(report.totals.available);
  const parts: string[] = [];
  if (proj) parts.push(`installed: ${proj}`);
  if (avail) parts.push(`available: ${avail}`);
  return parts.join(" · ");
}
